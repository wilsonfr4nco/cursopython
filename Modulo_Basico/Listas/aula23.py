"""
Listas em Python
fatiamento
append, insert, pop, del, clear, extend, +
min, max
range
"""
lista = [1, 2, 3, 4, 5, 'aula']
print(lista)
print(lista[1])
print(lista[0:3])
print(lista[1:3]) # de 1 até 2
print(lista[2:]) # iniciando de 2 até o final
print(lista[::2]) # igual a range
print(lista[::-1]) # lista mostrada de forma invertida
print()
lista2 = [1,2,3,4,5,6,7]
print(lista2)
lista2[5] = 'qualquer coisa' # inserir um valor em um índice da lista
print(lista2)